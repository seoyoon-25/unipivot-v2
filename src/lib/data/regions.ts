// 함수들
export function getSouthProvinces(): string[] {
  return southKoreaProvinces;
}

export function getNorthProvinces(): string[] {
  return northKoreaRegions.map((r) => r.name);
}

export function getNorthCities(province: string): string[] {
  const region = northKoreaRegions.find((r) => r.name === province);
  return region ? region.cities : [];
}

